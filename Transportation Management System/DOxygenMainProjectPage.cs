/// \section intro Program Introduction
/// The <b>TMS</b> application is a prototype for the <i>shipping</i>, and <i>logistics</i> industry.
/// It allows for the users to manage the purchasing, schedulling, monitoring and billing of multiple organizations that close a contract
/// to have their order delivered by a partner carrier, whether it is via train or truck. 
/// 
///
/// The application is written mainly in C#. For more information about this programming language, you can visit: 
/// - <A target=blank HREF="https://simple.wikipedia.org/wiki/C_Sharp_(programming_language)"> "C#" on Wikipedia </A>
///
/// \section features TMS Features:
/// - Generate a demand and we will find the best transportation method with the best price!
/// - Real Time updates and follow up of your delivery!
/// - We will take care of your order and assure its integrity and on-time delivery!
/// 
